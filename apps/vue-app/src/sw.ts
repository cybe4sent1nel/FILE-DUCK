/// <reference lib="webworker" />
import { clientsClaim } from 'workbox-core';
import { precacheAndRoute, cleanupOutdatedCaches } from 'workbox-precaching';
import { registerRoute, NavigationRoute } from 'workbox-routing';
import { NetworkFirst, CacheFirst } from 'workbox-strategies';
import { ExpirationPlugin } from 'workbox-expiration';
import { CacheableResponsePlugin } from 'workbox-cacheable-response';

declare let self: ServiceWorkerGlobalScope;

// Claim control immediately
self.skipWaiting();
clientsClaim();

// Clean up old caches
cleanupOutdatedCaches();

// Precache all assets generated by Vite
precacheAndRoute(self.__WB_MANIFEST);

// Cache Google Fonts
registerRoute(
  /^https:\/\/fonts\.googleapis\.com\/.*/i,
  new CacheFirst({
    cacheName: 'google-fonts-cache',
    plugins: [
      new ExpirationPlugin({
        maxEntries: 10,
        maxAgeSeconds: 60 * 60 * 24 * 365, // 1 year
      }),
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
    ],
  })
);

// Cache images
registerRoute(
  /\.(?:png|jpg|jpeg|svg|gif|webp)$/,
  new CacheFirst({
    cacheName: 'images-cache',
    plugins: [
      new ExpirationPlugin({
        maxEntries: 50,
        maxAgeSeconds: 60 * 60 * 24 * 30, // 30 days
      }),
    ],
  })
);

// Network First strategy for API calls (don't cache API responses when offline)
registerRoute(
  /\/api\/.*/,
  new NetworkFirst({
    cacheName: 'api-cache',
    plugins: [
      new ExpirationPlugin({
        maxEntries: 50,
        maxAgeSeconds: 60 * 5, // 5 minutes
      }),
    ],
  })
);

// Custom navigation handler - redirect to offline page when offline
const navigationHandler = async (options: any) => {
  const { request } = options;
  const url = new URL(request.url);
  
  // Pages to cache for offline use
  const cacheablePages = ['/history', '/offline', '/error'];
  const isCacheablePage = cacheablePages.some(page => url.pathname === page || url.pathname.startsWith(page + '/'));
  
  try {
    // Try to get from network first with timeout
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 3000); // 3 second timeout
    
    const response = await fetch(request, { 
      signal: controller.signal,
      cache: 'no-cache'
    });
    clearTimeout(timeoutId);
    
    // Cache the response only for cacheable pages
    if (response.ok && isCacheablePage) {
      const cache = await caches.open('pages-cache');
      cache.put(request, response.clone());
    }
    
    return response;
  } catch (error) {
    // Network request failed or timed out
    console.log('[SW] Network failed for:', url.pathname);
    
    // For cacheable pages, try to serve from cache
    if (isCacheablePage) {
      const cache = await caches.open('pages-cache');
      const cachedResponse = await cache.match(request);
      if (cachedResponse) {
        console.log('[SW] Serving from cache:', url.pathname);
        return cachedResponse;
      }
    }
    
    // For home page or any other page, always show offline page when network fails
    const cacheNames = await caches.keys();
    for (const cacheName of cacheNames) {
      const cache = await caches.open(cacheName);
      const offlineResponse = await cache.match('/offline');
      if (offlineResponse) {
        console.log('[SW] Serving offline page');
        return offlineResponse;
      }
    }
    
    // Final fallback
    return new Response('Offline - please check your connection', {
      status: 503,
      statusText: 'Service Unavailable',
      headers: new Headers({
        'Content-Type': 'text/html'
      })
    });
  }
};

// Register navigation route
const navigationRoute = new NavigationRoute(navigationHandler, {
  denylist: [/^\/api/], // Don't handle API routes
});

registerRoute(navigationRoute);

// Listen for messages from the client
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});
